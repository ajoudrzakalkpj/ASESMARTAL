package com.ajou.ase.raspberrycontrol;

public class RaspberrySALog {
	private int salogNumSeq;
	private String salogSAType;
	private String salogRaspberrySN;
	private String salogUpdateValue;
	private String salogLastupdatetime;
	
	public int getSalogNumSeq() {
		return salogNumSeq;
	}
	public void setSalogNumSeq(int salogNumSeq) {
		this.salogNumSeq = salogNumSeq;
	}
	public String getSalogSAType() {
		return salogSAType;
	}
	public void setSalogSAType(String salogSAType) {
		this.salogSAType = salogSAType;
	}
	public String getSalogRaspberrySN() {
		return salogRaspberrySN;
	}
	public void setSalogRaspberrySN(String salogRaspberrySN) {
		this.salogRaspberrySN = salogRaspberrySN;
	}
	public String getSalogUpdateValue() {
		return salogUpdateValue;
	}
	public void setSalogUpdateValue(String salogUpdateValue) {
		this.salogUpdateValue = salogUpdateValue;
	}
	public String getSalogLastupdatetime() {
		return salogLastupdatetime;
	}
	public void setSalogLastupdatetime(String salogLastupdatetime) {
		this.salogLastupdatetime = salogLastupdatetime;
	}
	
}
