package com.ajou.ase.raspberrycontrol;

public class RaspberrySA {

	private int saNumSeq;
	private String saType;
	private String saRaspberrySN;
	private String saUpdateValue;
	private String saReserveStatus;
	private String saLastupdatetime;
	
	
	public int getSaNumSeq() {
		return saNumSeq;
	}
	public void setSaNumSeq(int saNumSeq) {
		this.saNumSeq = saNumSeq;
	}
	public String getSaType() {
		return saType;
	}
	public void setSaType(String saType) {
		this.saType = saType;
	}
	public String getSaRaspberrySN() {
		return saRaspberrySN;
	}
	public void setSaRaspberrySN(String saRaspberrySN) {
		this.saRaspberrySN = saRaspberrySN;
	}
	
	public String getSaUpdateValue() {
		return saUpdateValue;
	}
	public void setSaUpdateValue(String saUpdateValue) {
		this.saUpdateValue = saUpdateValue;
	}
	public String getSaReserveStatus() {
		return saReserveStatus;
	}
	public void setSaReserveStatus(String saReserveStatus) {
		this.saReserveStatus = saReserveStatus;
	}
	public String getSaLastupdatetime() {
		return saLastupdatetime;
	}
	public void setSaLastupdatetime(String saLastupdatetime) {
		this.saLastupdatetime = saLastupdatetime;
	}
	
	
}
