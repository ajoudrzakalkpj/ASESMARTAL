package com.ajou.ase.raspberrycontrol;

public class Raspberry {

	private int raspberryNumSeq;
	private String raspberryID;
	private String raspberryNumSN;
	private String raspberryStatus;
	private String raspberryIPAddr;
	private String raspberrySSID;
	private String raspberryLastUpdateTime;
	private String raspberryServerAdmin;
	
	public int getRaspberryNumSeq() {
		return raspberryNumSeq;
	}
	public void setRaspberryNumSeq(int raspberryNumSeq) {
		this.raspberryNumSeq = raspberryNumSeq;
	}
	public String getRaspberryID() {
		return raspberryID;
	}
	public void setRaspberryID(String raspberryID) {
		this.raspberryID = raspberryID;
	}
	public String getRaspberryNumSN() {
		return raspberryNumSN;
	}
	public void setRaspberryNumSN(String raspberryNumSN) {
		this.raspberryNumSN = raspberryNumSN;
	}
	public String getRaspberryStatus() {
		return raspberryStatus;
	}
	public void setRaspberryStatus(String raspberryStatus) {
		this.raspberryStatus = raspberryStatus;
	}
	public String getRaspberryIPAddr() {
		return raspberryIPAddr;
	}
	public void setRaspberryIPAddr(String raspberryIPAddr) {
		this.raspberryIPAddr = raspberryIPAddr;
	}
	public String getRaspberrySSID() {
		return raspberrySSID;
	}
	public void setRaspberrySSID(String raspberrySSID) {
		this.raspberrySSID = raspberrySSID;
	}
	public String getRaspberryLastUpdateTime() {
		return raspberryLastUpdateTime;
	}
	public void setRaspberryLastUpdateTime(String raspberryLastUpdateTime) {
		this.raspberryLastUpdateTime = raspberryLastUpdateTime;
	}
	public String getRaspberryServerAdmin() {
		return raspberryServerAdmin;
	}
	public void setRaspberryServerAdmin(String raspberryServerAdmin) {
		this.raspberryServerAdmin = raspberryServerAdmin;
	}
		
}
